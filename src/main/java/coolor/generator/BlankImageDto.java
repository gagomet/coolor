public class BlankImageDto {
    protected Float width;
    protected Float height;
    protected Float area;
    protected Integer densityWidth;
    protected Integer densityHeight;
    protected Object colorspace;
    protected Integer code;
    protected String name;
    protected String city;
    protected String media;
    protected Integer luvers;

    public Float getWidth() {
        return width;
    }

    public void setWidth(Float width) {
        this.width = width;
    }

    public Float getHeight() {
        return height;
    }

    public void setHeight(Float height) {
        this.height = height;
    }

    public Float getArea() {
        return area;
    }

    public void setArea(Float area) {
        this.area = area;
    }

    public Integer getDensityWidth() {
        return densityWidth;
    }

    public void setDensityWidth(Integer densityWidth) {
        this.densityWidth = densityWidth;
    }

    public Integer getDensityHeight() {
        return densityHeight;
    }

    public void setDensityHeight(Integer densityHeight) {
        this.densityHeight = densityHeight;
    }

    public Object getColorspace() {
        return colorspace;
    }

    public void setColorspace(Object colorspace) {
        this.colorspace = colorspace;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getMedia() {
        return media;
    }

    public void setMedia(String media) {
        this.media = media;
    }

    public Integer getLuvers() {
        return luvers;
    }

    public void setLuvers(Integer luvers) {
        this.luvers = luvers;
    }
}
